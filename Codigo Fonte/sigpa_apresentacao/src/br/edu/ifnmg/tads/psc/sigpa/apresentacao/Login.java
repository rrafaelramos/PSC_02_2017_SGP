    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txt_nome.getText().equals("") || txt_senha.getText().equals("")){
            JOptionPane.showMessageDialog(null,"O usuário e a senha devem estar preenchidos");
        }else {
            Connection con = null;
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/sigpa", "root", "123");
                Statement stm = con.createStatement();
                String SQL = "Select * from funcionarios where username = '"+ txt_nome.getText()+"';";
                String TipoUsuario = "Select tipo from funcionario where senha = '"+ txt_nome.getText()+"';";
                funcionario.setTipo(TipoUsuario);
                ResultSet rs = stm.executeQuery(SQL);
                while(rs.next()){
                    String loginn = rs.getString("username");
                    String senhaa = rs.getString("senha");
                    String tipousuario = rs.getString("tipo");
                    
                    if(txt_nome.getText().equals(loginn) && txt_senha.getText().equals(senhaa)){
                        JOptionPane.showMessageDialog(null,"Seja bem vindo: " ,"",JOptionPane.INFORMATION_MESSAGE);
                        
                        TelaInicial tela = new TelaInicial();
                        tela.setVisible(true);
                        dispose();
                        switch (tipousuario) {
                                case  "Administrador":
                                    tela.habilitaAdministrador();
                                break;
                                
                                default: 
                                    tela.habilitaVendedor();
                                    break;
                        }
                    }else {
                        JOptionPane.showMessageDialog(null, "Senha ou Login Inválidos", "", JOptionPane.WARNING_MESSAGE);
                    }
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

